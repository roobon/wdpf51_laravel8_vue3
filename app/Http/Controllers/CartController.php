    {
        $id =  $request->prid;
        $product =  Product::find($id);
        $data = [
            'product_id' => $product->id,
            'quantity' => 1,
            'price' => $product->price,
            'user_id' => 1,
        ];
        Cart::create($data);
        return "Success";
    }

